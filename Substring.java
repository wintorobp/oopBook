
public class Substring {
    public static void main(String[] args) {
        String text = "I Love Espresso ";
        System.out.println(text.substring(2,7));
        System.out.println(text.length());
        System.out.println(text.indexOf("Love"));
        System.out.println(text.indexOf("jack"));
        System.out.println(text.indexOf("e"));

    }
}

